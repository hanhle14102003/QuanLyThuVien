NhaXuatBanDAO dao = new NhaXuatBanDAO();
        int row = -1;

    void innit() {
        this.setLocationRelativeTo(null);

        this.row = -1;
        this.filltable();
        this.updateStatus();
    }

    void filltable() {
        DefaultTableModel model = (DefaultTableModel) tblnxb.getModel();
        model.setRowCount(0);
        try {
            String keywork = txttimkiem.getText();
            List<NhaXuatBan> list = dao.selectByKeyword(keywork);
            for (NhaXuatBan TG : list) {
                Object[] row = {
                    TG.getMaNXB(),
                    TG.getTenNXB()
                };
                model.addRow(row);
            }
        } catch (Exception e) {
            MsgBox.alert(this, "Lỗi Truy Vấn Dữ Liệu");
        }
    }

    void setform(NhaXuatBan TG) {
        txtmanxb.setText(TG.getMaNXB());
        txttennxb.setText(TG.getTenNXB());
    }

    NhaXuatBan GetForm() {
        NhaXuatBan TG = new NhaXuatBan();
        TG.setMaNXB(txtmanxb.getText());
        TG.setTenNXB(txttennxb.getText());
        return TG;
    }

    void edit() {
        String Mangonngu = (String) tblnxb.getValueAt(this.row, 0);
        NhaXuatBan nn = dao.selectById(Mangonngu);
        this.setform(nn);
        Tabs.setSelectedIndex(0);
        this.updateStatus();
    }

    void updateStatus() {
        boolean edit = (this.row >= 0);
        boolean first = (this.row == 0);
        boolean last = (this.row == tblnxb.getRowCount() - 1);

        txtmanxb.setEditable(!edit);
        btnThem.setEnabled(!edit);
        btnXoa.setEnabled(edit);
        btnSua.setEnabled(edit);

        btnFirst.setEnabled(edit && !first);
        btnPrev.setEnabled(edit && !first);
        btnNext.setEnabled(edit && !last);
        btnLast.setEnabled(edit && !last);
    }

    void first() {
        this.row = 0;
        this.edit();
    }

    void prev() {
        if (this.row > 0) {
            this.row--;
            this.edit();
        }
    }

    void next() {
        if (this.row < tblnxb.getRowCount() - 1) {
            this.row++;
            this.edit();
        }
    }

    void last() {
        this.row = tblnxb.getRowCount() - 1;
        this.edit();
    }
    
    void clearn(){
        NhaXuatBan nn = new NhaXuatBan();
        this.setform(nn);
        this.row = -1;
        this.updateStatus();
    }
    
    void insert(){
        NhaXuatBan NN = GetForm();
        try {
            dao.insert(NN);
            this.filltable();
            this.clearn();
            MsgBox.alert(this, "Succes!");
        } catch (Exception e) {
            MsgBox.alert(this, "Error!");
        }
    }
    
    void update(){
        NhaXuatBan NN = GetForm();
        try {
            dao.update(NN);
            this.filltable();
            this.clearn();
            MsgBox.alert(this, "Succes!");
        } catch (Exception e) {
            MsgBox.alert(this, "Error!");
        }
    }
    
    void delete(){
        String Mann = (String) txtmanxb.getText();
        try {
            dao.delete(Mann);
            this.filltable();
            this.clearn();
            MsgBox.alert(this, "Succes!");
        } catch (Exception e) {
            MsgBox.alert(this, "Error!");
        }
    }
