    private void btnSuaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSuaActionPerformed
        // TODO add your handling code here:
        try {
            Class.forName("com.microsoft.sqlserver.jdbc.SQLServerDriver");
            String url = "jdbc:sqlserver://localhost;database=QuanLyThuVien";
            Connection con = DriverManager.getConnection(url, "sa", "");
            PreparedStatement ps = con.prepareStatement("update CTPhieuMuon set NgayTra = ?,TinhTrangSach = ?, TienPhat = ? where MaPM = ? and MaSach = ?");

            ps.setString(1, (XDate.toDate(txtngaytra.getText(), "MM/dd/yyyy")));
            ps.setString(2, txttrangthai.getText());
            ps.setString(3, txttienphat.getText());
            ps.setString(4, txtmaphieumuon.getText());
            ps.setString(5, cboamsach.getModel().getSelectedItem().toString());
            int kq = ps.executeUpdate();
            if (kq == 1) {
                JOptionPane.showMessageDialog(this, "sửa thành công");
            } else {
                JOptionPane.showMessageDialog(this, "sửa Thất Bại");
            }
            ps.close();
            con.close();
            FillTable();
        } catch (Exception e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_btnSuaActionPerformed

