    void init() {
        this.setLocationRelativeTo(null);
        this.filltablePM();
        FillTable();
        this.fillComboboxMasach();

    }

    void filltablePM() {
        DefaultTableModel model = (DefaultTableModel) tblphieumuon.getModel();
        model.setRowCount(0);
        try {
            String masach = (String) cboamsach.getSelectedItem();
            List<PhieuMuon> list = pmdao.selectAll();
            for (PhieuMuon pm : list) {
                Object[] row = {
                    pm.getMaPM(),
                    XDate.toString(pm.getNgayMuon(), "MM/dd/yyyy"),
                    XDate.toString(pm.getNgayTra(), "MM/dd/yyyy"),
                    pm.getSoNgayMuon(),
                    pm.getMaNV(),
                    pm.getGhiChu(),
                    pm.getTrangThai()
                };
                model.addRow(row);
            }
        } catch (Exception e) {
            MsgBox.alert(this, "Lỗi TRuy vấn Dữ Liệu PM!");
        }
    }

    void FillTable() {
        DefaultTableModel model = (DefaultTableModel) tblctphieumuon.getModel();
        model.setRowCount(0);
        try {
            List<ChiTietPhieuMuon> list = ctpmdao.selectAll();
            for (ChiTietPhieuMuon ct : list) {
                Object[] row = {
                    ct.getMaPM(),
                    ct.getMaSach(),
                    ct.getNgayTra(),
                    ct.getTinhTrangSach(),
                    ct.getTienPhat()
                };
                model.addRow(row);
            }
        } catch (Exception e) {
            MsgBox.alert(this, "Lỗi TRuy vấn Dữ Liệu PM!");
        }
    }

    void edit() {
        String MaPM = (String) tblctphieumuon.getValueAt(this.row, 0);
        ChiTietPhieuMuon ctpm = ctpmdao.selectById(MaPM);
        this.SetForm(ctpm);
        Tabs.setSelectedIndex(1);
        this.updateStatus();
    }

    void SetForm(ChiTietPhieuMuon CTPM) {
        txtmaphieumuon.setText(CTPM.getMaPM());
        cboamsach.getModel().setSelectedItem(CTPM.getMaSach());
        txtngaytra.setText(XDate.toString(CTPM.getNgayTra(), "MM/dd/yyyy"));
        txttrangthai.setText(CTPM.getTinhTrangSach());
        txttienphat.setText(String.valueOf(CTPM.getTienPhat()));
    }

    ChiTietPhieuMuon GetForm() {
        ChiTietPhieuMuon ctpm = new ChiTietPhieuMuon();
        ctpm.setMaPM(txtmaphieumuon.getText());
        ctpm.setMaSach(String.valueOf(cboamsach.getSelectedIndex()));
        ctpm.setNgayTra(XDate.toDate(txtngaytra.getText(), "MM/dd/yyyy"));
        ctpm.setTinhTrangSach(txttrangthai.getText());
        ctpm.setTienPhat(Integer.valueOf(txttienphat.getText()));
        return ctpm;
    }

    void clear() {
        ChiTietPhieuMuon ctpm = new ChiTietPhieuMuon();
        this.SetForm(ctpm);
        this.row = -1;
        this.updateStatus();
    }

    void insert() {
        ChiTietPhieuMuon kh = GetForm();
        try {
            ctpmdao.insert(kh);
            this.FillTable();
            this.clear();
            MsgBox.alert(this, "Thêm mới thành công!");
        } catch (HeadlessException e) {
            MsgBox.alert(this, "Thêm mới thất bại!");
        }
    }

    void update() {
        ChiTietPhieuMuon kh = GetForm();
        try {
            ctpmdao.update(kh);
            this.FillTable();
            this.clear();
            MsgBox.alert(this, "Cập nhật thành công!");
        } catch (Exception e) {
            MsgBox.alert(this, "Cập nhật thất bại!");
        }
    }

    void delete() {
        String makh = txtmaphieumuon.getText();
        try {
            ctpmdao.delete(makh);
            this.FillTable();
            this.clear();
            MsgBox.alert(this, "Xóa thành công!");
        } catch (Exception e) {
            MsgBox.alert(this, "Xóa thất bại!");
        }
    }

    void first() {
        this.row = 0;
        this.edit();
    }

    void prev() {
        if (this.row > 0) {
            this.row--;
            this.edit();
        }
    }

    void next() {
        if (this.row < tblctphieumuon.getRowCount() - 1) {
            this.row++;
            this.edit();
        }
    }

    void last() {
        this.row = tblctphieumuon.getRowCount() - 1;
        this.edit();
    }

    void updateStatus() {
        boolean edit = (this.row >= 0);
        boolean first = (this.row == 0);
        boolean last = (this.row == tblphieumuon.getRowCount() - 1);

        txtmaphieumuon.setEditable(!edit);
    }

    void fillComboboxMasach() {

        try {
            Class.forName("com.microsoft.sqlserver.jdbc.SQLServerDriver");
            String url = "jdbc:sqlserver://localhost;database=QuanLyThuVien";
            Connection con = DriverManager.getConnection(url, "sa", "");
            Statement st = con.createStatement();
            String sql = "Select MaSach from Sach";
            ResultSet rs = st.executeQuery(sql);
            while (rs.next()) {
                cboamsach.addItem(rs.getString("MaSach"));
            }
        } catch (Exception e) {
            MsgBox.alert(this, "Lỗi Truy vấn vào ComboBoxDM!");
        }

    }
