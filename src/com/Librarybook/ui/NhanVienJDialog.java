    void innit() {
        this.setLocationRelativeTo(null);

        this.row = -1;
        this.filltable();
       
    }

    void filltable() {
        DefaultTableModel model = (DefaultTableModel) tblNhanVien.getModel();
        model.setRowCount(0);
        try {
            String keywork = txtTimKiem.getText();
            List<NhanVien> list = dao.selectByKeyword(keywork);
            for (NhanVien nv : list) {
                String PW = nv.getMatKhau();
                String PW2 = "*************";
                if (Auth.isManager()) {
                    txtMatKhau.setEchoChar((char) 0);
                } else {
                    txtMatKhau.setEchoChar('*');
                }
                Object[] row = {
                    nv.getMaNV(),
                    nv.getTenNV(),
                    nv.getSoDienThoai(),
                    nv.isGioiTinh() ? "Nam" : "Nữ",
                    nv.getEmail(),
                    PW2,
                    nv.isVaiTRo() ? "Thủ Thư" : "Sinh Viên"
                };
                model.addRow(row);
            }
        } catch (Exception e) {
            MsgBox.alert(this, "Lỗi Truy Vấn Dữ Liệu!");
        }
    }

    void setFrom(NhanVien nv) {        
        txtmanv.setText(nv.getMaNV());
        txthoten.setText(nv.getTenNV());
        txtsdt.setText(nv.getSoDienThoai());
        rdonam.setSelected(nv.isGioiTinh());
        rdonu.setSelected(!nv.isGioiTinh());
        txtemail.setText(nv.getEmail());
        txtMatKhau.setText(nv.getMatKhau());
        rdothuthu.setSelected(nv.isVaiTRo());
        rdonhanvien.setSelected(!nv.isVaiTRo());

    }

    NhanVien getFrom() {
        NhanVien nv = new NhanVien();
        nv.setMaNV(txtmanv.getText());
        nv.setTenNV(txthoten.getText());
        nv.setSoDienThoai(txtsdt.getText());
        nv.setGioiTinh(rdonam.isSelected());
        nv.setEmail(txtemail.getText());
        nv.setMatKhau(txtMatKhau.getText());
        nv.setVaiTRo(rdothuthu.isSelected());

        return nv;
    }

  
    void edit() {
        String Manv = (String) tblNhanVien.getValueAt(this.row, 0);
        NhanVien nv = dao.selectById(Manv);
        this.setFrom(nv);
        tabs.setSelectedIndex(0);
        this.updateStatus();
    }

    void clear() {
        NhanVien nv = new NhanVien();
        this.setFrom(nv);
        this.row = -1;
        this.updateStatus();
    }

    void insert() {
        if (validatenv() == false) {

        } else {
            NhanVien nv = getFrom();
            try {
                dao.insert(nv);
                this.filltable();
                this.clear();
                MsgBox.alert(this, "Thêm Thành Công!");
            } catch (Exception e) {
                MsgBox.alert(this, "Error!");
            }
        }
    }

    void update() {
        NhanVien nv = getFrom();
        try {
            dao.update(nv);
            this.filltable();
            this.clear();
            MsgBox.alert(this, "Sửa Thành Công!");
        } catch (Exception e) {
            MsgBox.alert(this, "Error!");
        }
    }

    void delete() {
        if (!Auth.isManager()) {
            MsgBox.alert(this, "Bạn Không Có Quyền Xóa!");
        } else {
            String manv = txtmanv.getText();
            if (manv.equals(Auth.user.getMaNV())) {
                MsgBox.alert(this, "Bạn Không Được Xóa Chính Bạn!");
            } else if (MsgBox.confirm(this, "bạn có thwujc sự muốn xóa User này!")) {
                try {
                    dao.delete(manv);
                    this.filltable();
                    this.clear();
                    MsgBox.alert(this, "Xóa Thành Công");
                } catch (Exception e) {
                    MsgBox.alert(this, "Error!");
                }
            }
        }
    }

    void first() {
        this.row = 0;
        this.edit();
    }

    void prev() {
        if (this.row > 0) {
            this.row--;
            this.edit();
        }
    }

    void next() {
        if (this.row < tblNhanVien.getRowCount() - 1) {
            this.row++;
            this.edit();
        }
    }

    void last() {
        this.row = tblNhanVien.getRowCount() - 1;
        this.edit();
    }
    void updateStatus() {
        boolean edit = (this.row >= 0);
        boolean first = (this.row == 0);
        boolean last = (this.row == tblNhanVien.getRowCount() - 1);

        txtmanv.setEditable(!edit);
        btnThem.setEnabled(!edit);
        btnSua.setEnabled(edit);
        btnXoa.setEnabled(edit);

        btnFirst.setEnabled(edit && !first);
        btnPrev.setEnabled(edit && !first);
        btnNext.setEnabled(edit && !last);
        btnLast.setEnabled(edit && !last);
    }
