    boolean validatenv() {
        String manv = txtmanv.getText();
        if (dao.selectById(manv) != null) {
            MsgBox.alert(this, "Trùng mã Rồi");
            txtmanv.setBackground(Color.red);
            txtmanv.requestFocus();
            return false;
        } else if (txtmanv.getText().trim().length() == 0) {
            MsgBox.alert(this, "Mã Nhân Viên Không Được Bỏ Trống!");
            txtmanv.setBackground(Color.red);
            txtmanv.requestFocus();
        } else if (txthoten.getText().trim().length() == 0) {
            MsgBox.alert(this, "Tên NV không được bỏ trống");
            txthoten.setBackground(Color.red);
            txthoten.requestFocus();
            return false;
        } else if (txtsdt.getText().trim().length() == 0 || txtsdt.getText().length() < 10) {
            MsgBox.alert(this, "Số Điện Thoại Không Được Bỏ Trống Hoặc Số Điện Thoại Không Hợp Lệ!");
            txthoten.setBackground(Color.red);
            txthoten.requestFocus();
            return false;
        } else if (txtMatKhau.getText().trim().length() == 0) {
            MsgBox.alert(this, "Mật Khẩu Không Được Bỏ Trống!");
            txtMatKhau.setBackground(Color.red);
            txtMatKhau.requestFocus();
            return false;
        } else if (txtemail.getText().trim().length() == 0 || !txtemail.getText().matches("\\w+@\\w+(\\.\\w+){1,2}")) {
            MsgBox.alert(this, "Email Không Hợp Lệ!");
            txtemail.setBackground(Color.red);
            txtemail.requestFocus();
            return false;
        }
        try {
            int sdt = Integer.parseInt(txtsdt.getText());
        } catch (Exception e) {
            MsgBox.alert(this, "Số Điện Thoại Phải Là Số!");
            txtsdt.setBackground(Color.red);
            txtsdt.requestFocus();
            return false;
        }
        return true;
    }
