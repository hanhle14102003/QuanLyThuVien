NguoiMuonDao dao = new NguoiMuonDao();
    int row = -1;

    void innit() {
        this.setLocationRelativeTo(null);
        this.row = -1;
        this.filltable();
        this.updateStatus();
        
    }

    void filltable() {
        DefaultTableModel model = (DefaultTableModel) tblnguoimuon.getModel();
        model.setRowCount(0);
        try {
            String keywork = txtTimKiem.getText();
            List<NguoiMuon> list = dao.selectByKeyword(keywork);
            for (NguoiMuon nm : list) {
                Object[] row = {
                    nm.getMaNguoiMuon(),
                    nm.getTenNguoiMuon(),
                    nm.getSDT(),
                    nm.getDiaChi(),
                    nm.isGioiTinh() ? "Nam" : "Nữ",};
                model.addRow(row);
            }
        } catch (Exception e) {
            MsgBox.alert(this, "Lỗi Truy Vấn Dữ Liệu!");
        }
    }

    void setFrom(NguoiMuon nm) {
        txtmanm.setText(nm.getMaNguoiMuon());
        txttennm.setText(nm.getTenNguoiMuon());
        txtsdt.setText(nm.getSDT());
        txtdiachi.setText(nm.getDiaChi());
        rdonam.setSelected(nm.isGioiTinh());
        rdonu.setSelected(!nm.isGioiTinh());
    }

    NguoiMuon getFrom() {
        NguoiMuon nm = new NguoiMuon();
        nm.setMaNguoiMuon(txtmanm.getText());
        nm.setTenNguoiMuon(txttennm.getText());
        nm.setSDT(txtsdt.getText());
        nm.setDiaChi(txtdiachi.getText());
        nm.setGioiTinh(rdonam.isSelected());

        return nm;
    }

    void edit() {
        String MaNM = (String) tblnguoimuon.getValueAt(this.row, 0);
        NguoiMuon nm = dao.selectById(MaNM);
        this.setFrom(nm);
        tabs.setSelectedIndex(0);
        this.updateStatus();
    }

    void clear() {
        NguoiMuon nm = new NguoiMuon();
        this.setFrom(nm);
        this.row = -1;
        this.updateStatus();
    }

    void insert() {
        if (validatenv() == false) {

        } else {
            NguoiMuon nm = getFrom();
            try {
                dao.insert(nm);
                this.filltable();
                this.clear();
                MsgBox.alert(this, "Thêm Thành Công!");
            } catch (Exception e) {
                MsgBox.alert(this, "Error!");
            }
        }
    }

    void update() {
        NguoiMuon nm = getFrom();
        try {
            dao.update(nm);
            this.filltable();
            this.clear();
            MsgBox.alert(this, "Sửa Thành Công!");
        } catch (Exception e) {
            MsgBox.alert(this, "Error!");
        }
    }

    void delete() {
        String Mann = (String) txtmanm.getText();
        try {
            dao.delete(Mann);
            this.filltable();
            this.clear();
            MsgBox.alert(this, "Succes!");
        } catch (Exception e) {
            MsgBox.alert(this, "Error!");
        }
    }

    void first() {
        this.row = 0;
        this.edit();
    }

    void prev() {
        if (this.row > 0) {
            this.row--;
            this.edit();
        }
    }

    void next() {
        if (this.row < tblnguoimuon.getRowCount() - 1) {
            this.row++;
            this.edit();
        }
    }

    void last() {
        this.row = tblnguoimuon.getRowCount() - 1;
        this.edit();
    }

    void updateStatus() {
        boolean edit = (this.row >= 0);
        boolean first = (this.row == 0);
        boolean last = (this.row == tblnguoimuon.getRowCount() - 1);

        txtmanm.setEditable(!edit);
        btnThem.setEnabled(!edit);
        btnSua.setEnabled(edit);
        btnXoa.setEnabled(edit);

        btnFirst.setEnabled(edit && !first);
        btnPrev.setEnabled(edit && !first);
        btnNext.setEnabled(edit && !last);
        btnLast.setEnabled(edit && !last);
    }
