    boolean validatenv() {
        String manv = txtmanm.getText();
        if (dao.selectById(manv) != null) {
            MsgBox.alert(this, "Trùng mã Rồi");
            txtmanm.setBackground(Color.red);
            txtmanm.requestFocus();
            return false;
        } else if (txtmanm.getText().trim().length() == 0) {
            MsgBox.alert(this, "Mã Nhân Viên Không Được Bỏ Trống!");
            txtmanm.setBackground(Color.red);
            txtmanm.requestFocus();
        } else if (txttennm.getText().trim().length() == 0) {
            MsgBox.alert(this, "Tên NV không được bỏ trống");
            txttennm.setBackground(Color.red);
            txttennm.requestFocus();
            return false;
        } else if (txtsdt.getText().trim().length() == 0 || txtsdt.getText().length() < 10) {
            MsgBox.alert(this, "Số Điện Thoại Không Được Bỏ Trống Hoặc Số Điện Thoại Không Hợp Lệ!");
            txttennm.setBackground(Color.red);
            txttennm.requestFocus();
            return false;
        } else if (txtdiachi.getText().trim().length() == 0) {
            MsgBox.alert(this, "Địa chỉ không được bỏ trống");
            txtdiachi.setBackground(Color.red);
            txtdiachi.requestFocus();
            return false;
        }
        try {
            int sdt = Integer.parseInt(txtsdt.getText());
        } catch (Exception e) {
            MsgBox.alert(this, "Số Điện Thoại Phải Là Số!");
            txtsdt.setBackground(Color.red);
            txtsdt.requestFocus();
            return false;
        }
        return true;
    }

