    boolean validatetl() {
        String matl = txtmatheloai.getText();
        if (dao.selectById(matl) != null) {
            MsgBox.alert(this, "Trùng Mã Rồi!");
            txtmatheloai.setBackground(Color.red);
            txtmatheloai.requestFocus();
            return false;
        }
        if (txtmatheloai.getText().trim().length() == 0) {
            MsgBox.alert(this, "Mã Thể Loại Không Được Bỏ Trống!");
            txtmatheloai.setBackground(Color.red);
            txtmatheloai.requestFocus();
            return false;
        } else if (txttentheloai.getText().trim().length() == 0) {
            MsgBox.alert(this, "Thể Loại Không Được Để Trống");
            txttentheloai.setBackground(Color.red);
            txttentheloai.requestFocus();
            return false;
        }
        return true;
    }

