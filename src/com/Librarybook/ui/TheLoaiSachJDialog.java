    int row = -1;

    private void innit() {
        this.setLocationRelativeTo(null);

        this.filltable();
        this.row = -1;
        this.updateStatus();
    }

    void edit() {
        String MaTheLoai = (String) tbltheloaisach.getValueAt(this.row, 0);
        TheLoai nxb = dao.selectById(MaTheLoai);
        this.setForm(nxb);
        Tabs.setSelectedIndex(0);
        this.updateStatus();
    }

  void filltable() {
        DefaultTableModel model = (DefaultTableModel) tbltheloaisach.getModel();
        model.setRowCount(0);
        try {
            List<TheLoai> list = dao.selectAll();
            for (TheLoai TG : list) {
                Object[] row = {
                    TG.getMaTheLoai(),
                    TG.getTenTheLoai()
                };
                model.addRow(row);
            }
        } catch (Exception e) {
            MsgBox.alert(this, "Lỗi Truy Vấn Dữ Liệu");
        }
    }

    void setForm(TheLoai nxb) {
        txtmatheloai.setText(nxb.getMaTheLoai());
        txttentheloai.setText(nxb.getTenTheLoai());
    }

    void clear() {
        TheLoai nxb = new TheLoai();
        this.setForm(nxb);
        this.row = -1;
        this.updateStatus();
    }

    TheLoai getForm() {
        TheLoai nxb = new TheLoai();
        nxb.setMaTheLoai(txtmatheloai.getText());
        nxb.setTenTheLoai(txttentheloai.getText());
        return nxb;
    }

    void updateStatus() {
        boolean edit = (this.row >= 0);
        boolean first = (this.row == 0);
        boolean last = (this.row == tbltheloaisach.getRowCount() - 1);

        txtmatheloai.setEditable(!edit);
        btnThem.setEnabled(!edit);
        btnXoa.setEnabled(edit);
        btnSua.setEnabled(edit);

        btnFirst.setEnabled(edit && !first);
        btnPrev.setEnabled(edit && !first);
        btnNext.setEnabled(edit && !last);
        btnLast.setEnabled(edit && !last);
    }

    void first() {
        this.row = 0;
        this.edit();
    }

    void prev() {
        if (this.row > 0) {
            this.row--;
            this.edit();
        }
    }

    void next() {
        if (this.row < tbltheloaisach.getRowCount() - 1) {
            this.row++;
            this.edit();
        }
    }

    void last() {
        this.row = tbltheloaisach.getRowCount() - 1;
        this.edit();
    }

    void insert() {
        if (validatetl() == false) {

        } else {
            TheLoai nxb = getForm();
            try {
                dao.insert(nxb);
                this.filltable();
                this.clear();
                MsgBox.alert(this, "Succes!");
            } catch (Exception e) {
                MsgBox.alert(this, "Eror!");
            }
        }
    }

    void update() {
        TheLoai nxb = getForm();
        try {
            dao.update(nxb);
            this.filltable();
            MsgBox.alert(this, "Succes!");
        } catch (Exception e) {
            MsgBox.alert(this, "Error!");
        }
    }

    void delete() {
        String manv = txtmatheloai.getText();
        try {
            dao.delete(manv);
            this.filltable();
            this.clear();
            MsgBox.alert(this, "Succes!");
        } catch (Exception e) {
            MsgBox.alert(this, "Error!");
        }
    }
